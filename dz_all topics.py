# Задача 11
# У тебя есть два списка: keys = ["name", "age", "city"] и values = ["Анна", 25, "Москва"].
# Напиши код, который с помощью цикла и функции zip (или другого способа) создаёт словарь person,
# где элементы списка keys становятся ключами, а элементы списка values — соответствующими им значениями.
# Выведи полученный словарь на экран.

keys = ["name", "age", "city"]
values = ["Анна", 25, "Москва"]

person = {}
for key in range(len(keys)):
    person[keys[key]] = values[key]

print(person)



