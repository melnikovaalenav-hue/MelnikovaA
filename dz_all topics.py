# Задача 7
# Создай простой класс Rectangle (Прямоугольник).
# В конструкторе класса (__init__) определи два атрибута: width (ширина) и height (высота).
# Значения должны передаваться как аргументы при создании объекта.
# Создай метод area(self), который возвращает площадь прямоугольника (ширина * высота).
# Создай объект этого класса с шириной 5 и высотой 10.
# Вызови у созданного объекта метод area() и выведи результат на экран.

class Rectangle:
    def __init__(self, width, height):
        self.width = width
        self.height = height
    def area(self):
        return self.width * self.height

my_rectangle = Rectangle(5, 10)
print(my_rectangle.area())
