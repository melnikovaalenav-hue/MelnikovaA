# Вызови у созданного объекта метод info() и выведи результат на экран.

class Book:
    def __init__(self, title, author):
        self.title = title
        self.author = author
    def info(self):
        return f"Книга: {self.title}. Автор: {self.author}"

my_book = Book("1984", "Дж. Оруэлл")
print(my_book.info())

