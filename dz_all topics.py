# Функция должна принимать два аргумента: price (цена) и discount_percent (процент скидки).
# Внутри функции рассчитай сумму скидки по формуле: price * (discount_percent / 100).
# Функция должна возвращать итоговую цену после применения скидки (исходная цена минус сумма скидки).
# После описания функции вызови её с аргументами price=1000 и discount_percent=15,
# сохрани результат в переменную final_price и выведи эту переменную на экран.

def calculate_discount(price,discount_percent):
    result = price * (discount_percent / 100)
    return price - result

final_price = calculate_discount(1000, 15)
print(final_price)

