# Напишите функцию с именем calculate_discount. Она должна принимать два аргумента:
# price (исходная цена, число).
# discount_percent (процент скидки, число, по умолчанию равно 10).
# Функция должна вычислить сумму скидки по формуле: price * (discount_percent / 100).
# Затем функция должна вернуть (с помощью return) новую цену после применения скидки (т.е., price - сумма_скидки).
# После создания функции вызовите ее один раз, передав аргументы: price=2000 и discount_percent=15.
# Сохраните результат в переменную и выведите эту переменную в консоль (print).

def calculate_discount(price, discount_percent=10):
    discount_amount = price * (discount_percent / 100)
    return price - discount_amount

