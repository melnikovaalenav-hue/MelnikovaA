print(result)

# Задача 4.
# Раздел: Декораторы
# Напишите простой декоратор с именем my_decorator, который делает следующее:
# Перед вызовом декорируемой функции выводит в консоль строку: "Начало выполнения".
# Затем вызывает саму декорируемую функцию.
# После вызова декорируемой функции выводит в консоль строку: "Конец выполнения".
# Затем создайте (или возьмите готовую) простую функцию, например, say_hello, которая выводит "Привет!". Примените к ней декоратор @my_decorator.
# В конце скрипта просто вызовите декорированную функцию say_hello().

def my_decorator(func):
    def wrapper():
        print("Начало выполнения")
        func()
        print("Конец выполнения")
    return wrapper

@my_decorator
def say_hello():
    print("Привет!")

say_hello()

